    public function index()
    {
        $html = User::orderBy('nilai_mc', 'desc')
            ->orderBy('updated_at', 'desc')
            ->take(10)
            ->get();
        $css = User::orderBy('nilai_css', 'desc')
            ->orderBy('updated_at', 'desc')
            ->take(10)
            ->get();
        $js = User::orderBy('nilai_js', 'desc')
            ->orderBy('updated_at', 'desc')
            ->take(10)
            ->get();

        return view('leaderboard', compact('html', 'css', 'js'));
    }

